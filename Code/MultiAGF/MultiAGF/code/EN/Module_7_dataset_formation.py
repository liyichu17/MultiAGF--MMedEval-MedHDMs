import os
import json
import sys

"""
Summary: Used to construct the final illusion data based on the options determined by the vote.

This code iterates through the JSONL files in the `consensus_results` folder (i.e. the voting results folder).
It reads the `answer_idx` field (A, B, C, D, E) from each line of data,
then navigates to the `hallucinated_answers` folder to locate the corresponding hallucination file generated by the model, and extracts the data from the same line.
The final output files are merged and saved in the `final_data` folder.
"""

# ==========================================
# 1. Folder and mapping configuration
# ==========================================
consensus_dir = os.path.join("..", "..", "data", "intermediate_data", "consensus_results")
hallucinated_dir = os.path.join("..", "..", "data", "intermediate_data", "hallucinated_answers")
output_dir = os.path.join("..", "..", "data", "final_data")

os.makedirs(output_dir, exist_ok=True)

# Mapping of options A–E to model names
# Please ensure that the names here match the model names used when generating the hallucination data exactly
MODEL_MAP = {
    "A": "Model-A",
    "B": "Model-B",
    "C": "Model-C",
    "D": "Model-D",
    "E": "Model-E"
}

# If a command-line argument is passed, dynamically update the model mapping
if len(sys.argv) > 1:
    try:
        ext_configs = json.loads(sys.argv[1])
        new_map = {}
        keys = ["A", "B", "C", "D", "E"]
        for i, config in enumerate(ext_configs):
            if i < len(keys):
                new_map[keys[i]] = config["model_name"]
        MODEL_MAP = new_map
        print("💡 The model mapping has been dynamically loaded via external parameters.")
    except Exception as e:
        print(f"⚠️ Failed to parse external parameters, using built-in configuration: {e}")

# Base names for illusion files (standardised suffix)
BASE_HALLUCINATED_NAME = "MedBench3000_cl_hallucinated.jsonl"

# If there is a second command-line argument passed, override the hallucination filename configuration
if len(sys.argv) > 2:
    try:
        base_configs = json.loads(sys.argv[2])
        BASE_HALLUCINATED_NAME = base_configs.get("base_filename", BASE_HALLUCINATED_NAME)
        print(f"💡 The base filename has been loaded via external parameters: {BASE_HALLUCINATED_NAME}")
    except Exception as e:
        print(f"⚠️ Failed to parse base filename parameters: {e}")

def get_jsonl_files(folder):
    return [f for f in os.listdir(folder) if f.endswith('.jsonl')]


# ==========================================
# 2. Main processing workflow
# ==========================================
print("Compiling the final illusion dataset based on the voting results...\n")

for filename in get_jsonl_files(consensus_dir):
    choicereview_path = os.path.join(consensus_dir, filename)

    # ==========================================
    # 2.1 Preload the contents of five hallucination files generated by the model into memory
    # ==========================================
    model_lines = {}
    for option_key, model_name in MODEL_MAP.items():
        # Support for special characters in model names (consistent with the secure naming logic used when generating files)
        safe_model_name = model_name.replace('/', '_').replace(':', '_')

        # Path to the target file to be concatenated: for example, ‘qwen3.6-flash_MedBench10_cl_hallucinated.jsonl’
        model_filename = f"{safe_model_name}_{BASE_HALLUCINATED_NAME}"
        model_path = os.path.join(hallucinated_dir, model_filename)

        if os.path.exists(model_path):
            with open(model_path, 'r', encoding='utf-8') as f:
                model_lines[option_key] = f.readlines()
        else:
            print(f"⚠️ Warning: The hallucination file for model [{option_key}] cannot be found: {model_path}")
            model_lines[option_key] = []

    # ==========================================
    # 2.2 Processing the vote results file and assembling the final data line by line
    # ==========================================
    with open(choicereview_path, 'r', encoding='utf-8') as f:
        review_lines = f.readlines()

    output_lines = []
    for idx, line in enumerate(review_lines):
        try:
            data = json.loads(line)
        except Exception:
            output_lines.append('{}')
            continue

        answer_idx = data.get('answer_idx', None)

        # Check: If the row contains valid voting results (A–E) and the corresponding model’s hallucination file data is sufficiently long
        if answer_idx in MODEL_MAP and idx < len(model_lines[answer_idx]):
            # Extract the corresponding line of phantom data generated by the selected model and remove the line break at the end
            output_lines.append(model_lines[answer_idx][idx].rstrip('\n'))
        else:
            # If there is no consensus and the field is left blank, or if the data for that row is missing, return an empty object
            output_lines.append('{}')

    # ==========================================
    # 2.3 Outputting the assembled data
    # ==========================================
    clean_filename = "final_hal_data.jsonl"
    output_path = os.path.join(output_dir, clean_filename)

    with open(output_path, 'w', encoding='utf-8') as f:
        for l in output_lines:
            f.write(l + '\n')

    print(f"✅ The file {clean_filename} has been processed.")

print(f"\n🎉 All assembly tasks have been completed! The final data has been exported to the directory：{output_dir}")